public class Estudiante {
    private String studentnam;
    private String studentcod;


    public Estudiante(String studentnam_param, String studentcod_param) {
        this.studentnam = studentnam_param;
        this.studentcod = studentcod_param;
    }


    public String getStudentnam() {
        return studentnam;
    }


    public void setStudentnam(String studentnam) {
        this.studentnam = studentnam;
    }


    public String getStudentcod() {
        return studentcod;
    }


    public void setStudentcod(String studentcod) {
        this.studentcod = studentcod;
    }


  
    

}
